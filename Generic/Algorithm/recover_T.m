function [R_all,t_all] = recover_T(A,X_3,X_4,Y_3,Y_4,Z_4,b,R21,t_y)
    R_all=[]; t_all=[];
    for i=1:4
        R11=-A(3,2)/A(3,1)*R21(i)+( A(3,6)*A(1,7)-A(1,6)*A(3,7)  )/( A(1,6)*A(3,1))*t_y(i)+ (   A(1,6)*b(3)-A(3,6)*b(1)   )/( A(1,6)*A(3,1));
        R31=-A(4,2)/A(4,3)*R21(i)+( A(4,8)*A(2,7)-A(2,8)*A(4,7)  )/( A(2,8)*A(4,3))*t_y(i)+ (   A(2,8)*b(4)-A(4,8)*b(2)   )/( A(2,8)*A(4,3));
        R22=-X_3/Y_3*R21(i)-1/Y_3*t_y(i);
        R23=(X_3*Y_4-X_4*Y_3)/(Y_3*Z_4)*R21(i)+(Y_4-Y_3)/(Y_3*Z_4)*t_y(i);
        
        t_all(1,i)=-A(1,7)/A(1,6)*t_y(i)+b(1)/A(1,6);
        t_all(2,i)=t_y(i);
        t_all(3,i)=-A(2,7)/A(2,8)*t_y(i)+b(2)/A(2,8);
    
        else_R=1/(R22^2+R23^2)*[-R11*R21(i)*R22+R23*R31;
            -R11*R21(i)*R23-R22*R31;
            -R21(i)*R22*R31-R11*R23;
            -R21(i)*R23*R31+R11*R22];
        
        R_all(:,:,i)=[R11,else_R(1),else_R(2);
              R21(i),R22,R23;
              R31,else_R(3),else_R(4)];
    end

end

