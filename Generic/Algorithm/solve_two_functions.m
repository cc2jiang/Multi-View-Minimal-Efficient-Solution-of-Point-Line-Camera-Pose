function [R21_est,t_y_est] = solve_two_functions(A,X_3,X_4,Y_3,Y_4,Z_4,B,x_real)
    a1=1+(     A(3,2)/A(3,1)  )^2+(A(4,2)/A(4,3))^2;
    b1=-2*A(3,2)*(A(3,6)*A(1,7)-A(1,6)*A(3,7))/(  A(1,6)*A(3,1)^2  )  -  2*A(4,2)*(A(4,8)*A(2,7)-A(2,8)*A(4,7))/(  A(2,8)*A(4,3)^2  );
    c1=( (  A(3,6)*A(1,7)-A(1,6)*A(3,7)  )/(A(1,6)*A(3,1))     )^2   +    ( (  A(4,8)*A(2,7)-A(2,8)*A(4,7)  )/(A(2,8)*A(4,3))     )^2;
    d1=-2*A(3,2)*(  A(1,6)*B(3)-A(3,6)*B(1)  )/  (  A(1,6)*A(3,1)^2  )   -2*A(4,2)*(  A(2,8)*B(4)-A(4,8)*B(2)  )/  (  A(2,8)*A(4,3)^2  );
    e1=2*(  A(3,6)*A(1,7)-A(1,6)*A(3,7)  )*(A(1,6)*B(3)-A(3,6)*B(1))/(A(1,6)*A(3,1))^2    +    2*(  A(4,8)*A(2,7)-A(2,8)*A(4,7)  )*(A(2,8)*B(4)-A(4,8)*B(2))/(A(2,8)*A(4,3))^2;
    f1=(  (A(1,6)*B(3)-A(3,6)*B(1))/(A(1,6)*A(3,1))  )^2  +   (  (A(2,8)*B(4)-A(4,8)*B(2))/(A(2,8)*A(4,3))  )^2-1;
    a2=1+(X_3/Y_3)^2+((X_3*Y_4-X_4*Y_3)/(Y_3*Z_4))^2;
    b2=(  2*X_3*(Z_4)^2+2*(X_3*Y_4-X_4*Y_3)*(Y_4-Y_3)     )  /  (Y_3*Z_4)^2;
    c2=(1/Y_3)^2+(  (Y_4-Y_3)/(Y_3*Z_4)  )^2;
    f2=-1;
   
    
    
    %% 
    M1=a2*(b1*c2-b2*c1)^2 + b2*(a2*c1-a1*c2)*(b1*c2-b2*c1)+c2*(a2*c1-a1*c2)^2;
    M2=2*a2*e1*c2*(b1*c2-b2*c1)   +  b2*( e1*c2*(a2*c1-a1*c2)-d1*c2*(b1*c2-b2*c1) )  -  2*d1*c2^2*(a2*c1-a1*c2);
    M3=a2*(e1*c2)^2 + b2*( -d1*e1*c2^2+(f2*c1-f1*c2)*(b1*c2-b2*c1) ) + c2*( (d1*c2)^2+2*(a2*c1-a1*c2)*(f2*c1-f1*c2) ) + f2*(b1*c2-b2*c1)^2;
    M4=b2*e1*c2*(f2*c1-f1*c2)-2*d1*c2^2*(f2*c1-f1*c2)+2*f2*e1*c2*(b1*c2-b2*c1);
    M5=c2*(f2*c1-f1*c2)^2+f2*(e1*c2)^2;
    
    %%

    roots_of_equation = roots([M1 M2 M3 M4 M5]);
    R21_est=double(roots_of_equation);

    t_y_est = ((a2 * c1 - a1 * c2) * R21_est.^2 - d1 * c2 * R21_est - f1 * c2 + f2 * c1) ./ ((b1 * c2 - b2 * c1) * R21_est + e1 * c2);

%
    
    
    
end

