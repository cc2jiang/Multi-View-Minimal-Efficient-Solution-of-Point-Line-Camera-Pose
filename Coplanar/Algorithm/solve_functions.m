function [t_y_est] = solve_functions(A,X_3,X_4,Y_3,Y_4,B,x_real)
    c1=(Y_3-Y_4)/(X_3*Y_4-X_4*Y_3);
    c2=(   (A(3,6)*A(1,7)-A(1,6)*A(3,7))/(A(1,6)*A(3,1))     -...
            A(3,2)*(Y_3-Y_4)/(  A(3,1)*(X_3*Y_4-X_4*Y_3)  )         );
    d2=(A(1,6)*B(3)-A(3,6)*B(1))/(A(1,6)*A(3,1));
    c3=(   (A(4,8)*A(2,7)-A(2,8)*A(4,7))/(A(2,8)*A(4,3))     -...
            A(4,2)*(Y_3-Y_4)/(  A(4,3)*(X_3*Y_4-X_4*Y_3)  )         );
    d3=(A(2,8)*B(4)-A(4,8)*B(2))/(A(2,8)*A(4,3));
    
    R21=c1*x_real(7);
    R11=c2*x_real(7)+d2;
    R31=c3*x_real(7)+d3;
    

    
    a=c1^2+c2^2+c3^2;
    b=2*(c2*d2+c3*d3);
    c=d2^2+d3^2-1;
    
%     a*x_real(7)^2+b*x_real(7)+c;
    
    t_y_est(1)=(-b+sqrt(b^2-4*a*c))/(2*a);
    t_y_est(2)=(-b-sqrt(b^2-4*a*c))/(2*a);
    
    
    
end

